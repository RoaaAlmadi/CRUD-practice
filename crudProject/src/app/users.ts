export class Users {
  name: string;
  surname: string;
  age: number;
  avatar: string;
  id: string;  

  constructor(name: string, surname: string, age: number, avatar: string, id: string) {
      this.name = name;
      this.surname = surname;
      this.age = age;
      this.avatar = avatar;
      this.id = id
  }

}
